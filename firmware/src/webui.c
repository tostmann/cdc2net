// POST JSON: {tcp_port,static_ip,ip,mask,gw,dns,wdt_enable,wdt_timeout_s}.
// Overlays the current config; saved to NVS, applied on reboot.
static esp_err_t h_config_post(httpd_req_t *req)
{
    char body[400];
    read_body(req, body, sizeof(body));

    cdc2net_cfg_t c;
    config_load(&c);

    char v[24];
    if (json_field(body, "tcp_port", v, sizeof(v)) == 0) {
        int p = atoi(v);
        if (p > 0 && p < 65536) c.tcp_port = (uint16_t)p;
    }
    c.static_ip = json_field_truthy(body, "static_ip");
    json_field(body, "ip",   c.ip,   sizeof(c.ip));
    json_field(body, "mask", c.mask, sizeof(c.mask));
    json_field(body, "gw",   c.gw,   sizeof(c.gw));
    json_field(body, "dns",  c.dns,  sizeof(c.dns));
    c.wdt_enable = json_field_truthy(body, "wdt_enable");
    if (json_field(body, "wdt_timeout_s", v, sizeof(v)) == 0) {
        int t = atoi(v);
        if (t >= 30) c.wdt_timeout_s = (uint32_t)t;
    }

    if (c.static_ip && (c.ip[0] == '\0' || c.mask[0] == '\0' || c.gw[0] == '\0')) {
        httpd_resp_send_err(req, HTTPD_400_BAD_REQUEST, "static IP needs ip/mask/gw");
        return ESP_FAIL;
    }
    if (config_save(&c) != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "save failed");
        return ESP_FAIL;
    }
    httpd_resp_set_type(req, "application/json");
    return httpd_resp_send(req, "{\"saved\":true,\"reboot_required\":true}",
                           HTTPD_RESP_USE_STRLEN);
}

// ───── /api/serial — per-device line coding (baud/bits/parity/stop) ──────

static const char *lcsrc_str(uint8_t s) {
    return s == 1 ? "nvs" : (s == 2 ? "rfc2217" : "default");
}

static esp_err_t h_serial_get(httpd_req_t *req)
{
    source_usb_serial_info_t si;
    source_usb_get_serial_info(&si);
    serialcfg_item_t items[SERIALCFG_MAX];
    int nitems = serialcfg_list(items, SERIALCFG_MAX);

    char key_esc[80], ser_esc[80];
    json_escape(si.key,    key_esc, sizeof(key_esc));
    json_escape(si.serial, ser_esc, sizeof(ser_esc));

    char buf[1024];
    int n = snprintf(buf, sizeof(buf),
        "{\"attached\":{\"connected\":%s,\"is_vcp\":%s,\"vid\":\"%04X\",\"pid\":\"%04X\","
          "\"serial\":\"%s\",\"key\":\"%s\"},"
        "\"effective\":{\"baud\":%u,\"bits\":%u,\"parity\":%u,\"stop\":%u,\"source\":\"%s\"},"
        "\"stored\":[",
        si.connected ? "true" : "false", si.is_vcp ? "true" : "false", si.vid, si.pid,
        ser_esc, key_esc,
        (unsigned)si.baud, si.bits, si.parity, si.stop, lcsrc_str(si.lc_source));
    for (int i = 0; i < nitems && n > 0 && n < (int)sizeof(buf) - 96; i++) {
        char k_esc[80];
        json_escape(items[i].key, k_esc, sizeof(k_esc));
        n += snprintf(buf + n, sizeof(buf) - n,
            "%s{\"key\":\"%s\",\"baud\":%u,\"bits\":%u,\"parity\":%u,\"stop\":%u}",
            i ? "," : "", k_esc, (unsigned)items[i].lc.baud,
            items[i].lc.bits, items[i].lc.parity, items[i].lc.stop);
    }
    if (n > 0 && n < (int)sizeof(buf) - 3)
        n += snprintf(buf + n, sizeof(buf) - n, "]}");
    if (n <= 0 || n >= (int)sizeof(buf)) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "buf overflow");
        return ESP_FAIL;
    }
    httpd_resp_set_type(req, "application/json");
    httpd_resp_set_hdr(req, "Cache-Control", "no-store");
    return httpd_resp_send(req, buf, n);
}

// POST {key,baud,bits,parity,stop}  — store the per-device default (applied
// live if that device is attached and no RFC2217 client owns the port).
// POST {key,delete:true}            — forget the entry.
static esp_err_t h_serial_post(httpd_req_t *req)
{
    char body[256];
    read_body(req, body, sizeof(body));

    char key[SERIALCFG_KEY_LEN];
    if (json_field(body, "key", key, sizeof(key)) != 0 || key[0] == '\0') {
        httpd_resp_send_err(req, HTTPD_400_BAD_REQUEST, "key required");
        return ESP_FAIL;
    }
    if (json_field_truthy(body, "delete")) {
        serialcfg_delete(key);
        // If we just deleted the entry for the *attached* device (and no RFC2217
        // client owns the port), re-apply the global default live — otherwise
        // the effective line coding lingers at the now-orphaned NVS value still
        // labelled "nvs" instead of falling back to "default".  Mirrors the
        // upsert apply-live guard below; runs in the httpd task (no sink mutex
        // held), so apply_line_coding's tx_mtx never nests.
        bool reverted = false;
        source_usb_serial_info_t si;
        source_usb_get_serial_info(&si);
        if (si.connected && si.lc_source != 2 && strcmp(si.key, key) == 0) {
            serialcfg_lc_t d = serialcfg_default();
            reverted = (source_usb_apply_line_coding(d.baud, d.bits, d.parity,
                                                     d.stop, 0) == ESP_OK);
        }
        char resp[48];
        int n = snprintf(resp, sizeof(resp), "{\"deleted\":true,\"reverted\":%s}",
                         reverted ? "true" : "false");
        httpd_resp_set_type(req, "application/json");
        return httpd_resp_send(req, resp, n);
    }

    char v[16];
    serialcfg_lc_t lc = serialcfg_default();
    if (json_field(body, "baud", v, sizeof(v)) == 0) {
        long b = atol(v); if (b >= 300 && b <= 4000000) lc.baud = (uint32_t)b;
    }
    if (json_field(body, "bits", v, sizeof(v)) == 0) {
        int b = atoi(v); if (b >= 5 && b <= 8) lc.bits = (uint8_t)b;
    }
    if (json_field(body, "parity", v, sizeof(v)) == 0) {
        int p = atoi(v); if (p >= 0 && p <= 4) lc.parity = (uint8_t)p;
    }
    if (json_field(body, "stop", v, sizeof(v)) == 0) {
        int s = atoi(v); if (s >= 0 && s <= 2) lc.stop = (uint8_t)s;
    }

    if (serialcfg_upsert(key, &lc) != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "save failed");
        return ESP_FAIL;
    }

    // Apply now if this is the attached device and no RFC2217 client controls it.
    bool applied = false;
    source_usb_serial_info_t si;
    source_usb_get_serial_info(&si);
    if (si.connected && si.lc_source != 2 && strcmp(si.key, key) == 0)
        applied = (source_usb_apply_line_coding(lc.baud, lc.bits, lc.parity,
                                                lc.stop, 1) == ESP_OK);

    char resp[64];
    int n = snprintf(resp, sizeof(resp), "{\"saved\":true,\"applied\":%s}",
                     applied ? "true" : "false");
    httpd_resp_set_type(req, "application/json");
    return httpd_resp_send(req, resp, n);
}

// ───── /api/wifi — set creds / scan / forget ────────────────────────────

static esp_err_t h_wifi(httpd_req_t *req)
{
    char body[256];
    read_body(req, body, sizeof(body));
    char ssid[33] = {0}, pass[65] = {0};
    if (json_field(body, "ssid", ssid, sizeof(ssid)) != 0 || ssid[0] == '\0') {
        httpd_resp_send_err(req, HTTPD_400_BAD_REQUEST, "missing ssid");
        return ESP_FAIL;
    }
    json_field(body, "pass", pass, sizeof(pass));   // empty = open net
    if (net_persist_creds(ssid, pass) != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "persist failed");
        return ESP_FAIL;
    }
    ESP_LOGW(TAG, "WiFi creds saved via WebUI (ssid='%s')", ssid);
    httpd_resp_set_type(req, "application/json");
    return httpd_resp_send(req, "{\"saved\":true,\"reboot_required\":true}",
                           HTTPD_RESP_USE_STRLEN);
}

static esp_err_t h_wifi_scan(httpd_req_t *req)
{
    static net_scan_entry_t entries[24];
    size_t got = 0;
    if (net_scan(entries, sizeof(entries) / sizeof(entries[0]), &got) != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "scan failed");
        return ESP_FAIL;
    }
    static char buf[1800];
    int p = snprintf(buf, sizeof(buf), "[");
    for (size_t i = 0; i < got && p < (int)sizeof(buf) - 128; i++) {
        char esc[66] = {0};
        json_escape(entries[i].ssid, esc, sizeof(esc));
        p += snprintf(buf + p, sizeof(buf) - p,
                      "%s{\"ssid\":\"%s\",\"rssi\":%d,\"ch\":%u,\"auth\":\"%s\"}",
                      i ? "," : "", esc, (int)entries[i].rssi,
                      (unsigned)entries[i].channel, authmode_str(entries[i].authmode));
    }
    p += snprintf(buf + p, sizeof(buf) - p, "]");
    httpd_resp_set_type(req, "application/json");
    httpd_resp_set_hdr(req, "Cache-Control", "no-store");
    return httpd_resp_send(req, buf, p);
}

static esp_err_t h_wifi_reset(httpd_req_t *req)
{
    if (net_clear_creds() != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "nvs erase failed");
        return ESP_FAIL;
    }
    httpd_resp_set_type(req, "application/json");
    httpd_resp_send(req, "{\"reset\":true,\"reboot\":true}", HTTPD_RESP_USE_STRLEN);
    xTaskCreate(delayed_reboot_task, "reboot-cred", 2048, NULL, 5, NULL);
    return ESP_OK;
}

// ───── /api/update/install — HTTPS-pull OTA from the release server ─────

static esp_err_t h_update_install(httpd_req_t *req)
{
    esp_err_t e = ota_install_start();
    httpd_resp_set_type(req, "application/json");
    if (e == ESP_ERR_INVALID_STATE) {
        return httpd_resp_send(req, "{\"installing\":true,\"note\":\"already running\"}",
                               HTTPD_RESP_USE_STRLEN);
    }
    if (e != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "install start failed");
        return ESP_FAIL;
    }
    return httpd_resp_send(req, "{\"installing\":true}", HTTPD_RESP_USE_STRLEN);
}

// ───── init ─────────────────────────────────────────────────────────────

esp_err_t webui_init(uint16_t port)
{
    if (s_http) return ESP_OK;

    httpd_config_t cfg   = HTTPD_DEFAULT_CONFIG();
    cfg.server_port      = port ? port : 80;
    cfg.lru_purge_enable = true;
    cfg.max_uri_handlers = 22;
    cfg.max_open_sockets = 4;
    cfg.recv_wait_timeout = 30;
    cfg.send_wait_timeout = 30;
    cfg.stack_size       = 8192;   // h_ota needs headroom (recv buf + flash + lwIP)

    if (httpd_start(&s_http, &cfg) != ESP_OK) {
        ESP_LOGE(TAG, "httpd_start failed");
        return ESP_FAIL;
    }

    httpd_uri_t u;
    u = (httpd_uri_t){ .uri="/",                 .method=HTTP_GET,  .handler=h_index };        httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/status",       .method=HTTP_GET,  .handler=h_status };       httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/log",          .method=HTTP_GET,  .handler=h_log };          httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/reboot",       .method=HTTP_POST, .handler=h_reboot };       httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/ota",          .method=HTTP_POST, .handler=h_ota };          httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/update/check", .method=HTTP_GET,  .handler=h_update_check }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/update/check", .method=HTTP_POST, .handler=h_update_check }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/update/install",.method=HTTP_POST,.handler=h_update_install }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/config",        .method=HTTP_GET, .handler=h_config_get };  httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/config",        .method=HTTP_POST,.handler=h_config_post }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/serial",        .method=HTTP_GET, .handler=h_serial_get };  httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/serial",        .method=HTTP_POST,.handler=h_serial_post }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/wifi",          .method=HTTP_POST,.handler=h_wifi };        httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/wifi/scan",     .method=HTTP_GET, .handler=h_wifi_scan };   httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/api/wifi/reset",    .method=HTTP_POST,.handler=h_wifi_reset };  httpd_register_uri_handler(s_http, &u);

    // Captive-portal probes (Android/Apple/Windows) → 302 to "/".
    u = (httpd_uri_t){ .uri="/generate_204",       .method=HTTP_GET, .handler=h_captive_redirect }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/gen_204",            .method=HTTP_GET, .handler=h_captive_redirect }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/hotspot-detect.html",.method=HTTP_GET, .handler=h_captive_redirect }; httpd_register_uri_handler(s_http, &u);
    u = (httpd_uri_t){ .uri="/ncsi.txt",           .method=HTTP_GET, .handler=h_captive_redirect }; httpd_register_uri_handler(s_http, &u);
    httpd_register_err_handler(s_http, HTTPD_404_NOT_FOUND, h_not_found);

    ESP_LOGI(TAG, "WebUI on http://%s:%u/", net_ip_str(), cfg.server_port);
    return ESP_OK;
}
