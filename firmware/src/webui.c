// Minimal JSON string-field extract (no nested escapes). Returns 0 on success.
static int json_field(const char *in, const char *key, char *out, size_t out_cap)
{
    char needle[40];
    snprintf(needle, sizeof(needle), "\"%s\"", key);
    const char *p = strstr(in, needle);
    if (!p) return -1;
    p = strchr(p + strlen(needle), ':');
    if (!p) return -1;
    p++;
    while (*p == ' ' || *p == '\t') p++;
    size_t n = 0;
    if (*p == '"') {                       // quoted string value
        p++;
        while (*p && *p != '"' && n + 1 < out_cap) {
            if (*p == '\\' && p[1]) p++;
            out[n++] = *p++;
        }
        out[n] = '\0';
        return (*p == '"') ? 0 : -1;
    }
    // unquoted value (number / true / false / null) — read to delimiter.
    while (*p && *p != ',' && *p != '}' && *p != ']' &&
           *p != ' ' && *p != '\t' && *p != '\n' && *p != '\r' && n + 1 < out_cap) {
        out[n++] = *p++;
    }
    out[n] = '\0';
    return n ? 0 : -1;
}

// JSON boolean / "true" / 1 lookup.  false if missing or non-truthy.
static bool json_field_truthy(const char *in, const char *key)
{
    char needle[40];
    snprintf(needle, sizeof(needle), "\"%s\"", key);
    const char *p = strstr(in, needle);
    if (!p) return false;
    p = strchr(p + strlen(needle), ':');
    if (!p) return false;
    p++;
    while (*p == ' ' || *p == '\t' || *p == '"') p++;
    return strncmp(p, "true", 4) == 0 || *p == '1';
}

static int read_body(httpd_req_t *req, char *buf, size_t cap)
{
    int total = 0;
    while (total < (int)cap - 1) {
        int r = httpd_req_recv(req, buf + total, (size_t)((int)cap - 1 - total));
        if (r <= 0) {
            if (r == HTTPD_SOCK_ERR_TIMEOUT) continue;
            break;
        }
        total += r;
    }
    buf[total] = '\0';
    return total;
}

